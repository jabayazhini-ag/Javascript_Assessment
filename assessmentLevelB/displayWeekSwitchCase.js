console.log("Display Weekday Using Switch Case Conditional Statemen")

let day = 5; // Week Day number

console.log("Enter the number = " + day)

switch (day) {
    case 1:
        console.log("Monday");
        break;
    case 2:
        console.log("Tuesday");
        break;
    case 3:
        console.log("Wednesday");
        break;
    case 4:
        console.log("Thursday");
        break;
    case 5:
        console.log("Friday");
        break;
    case 6:
        console.log("Saturday");
        break;
    case 7:
        console.log("Sunday");
        break;
    default:
        console.log("Invalid day number. Please Enter the 1 to 7 Values Only");
        break;
}




